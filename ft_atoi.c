#include "libft.h"

int	ft_atoi(const char *str)
{
	long	nbr;
	long	i;
	int		nbrminus;

	nbr = 0;
	i = 0;
	nbrminus = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			nbrminus *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + str[i] - '0';
		i++;
	}
	return (nbr * nbrminus);
}
